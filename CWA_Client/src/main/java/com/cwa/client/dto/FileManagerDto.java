package com.cwa.client.dto;

import java.sql.Timestamp;

public class FileManagerDto extends BaseDto{

	private Integer id;
	private Timestamp uploadTime;// 上传时间
	private String filePath;// 文件路径
	private String accountId;// 登录账户
	private Integer accountType;// 账户类型（1：总公司，2：分公司，3：业务员，4：代理，5：广告商，6：客户）'
	private Integer status;// 文件状态(1=未处理、2=已处理、3=已删除)'

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Timestamp getUploadTime() {
		return uploadTime;
	}

	public void setUploadTime(Timestamp uploadTime) {
		this.uploadTime = uploadTime;
	}

	public String getFilePath() {
		return filePath;
	}

	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}

	public String getAccountId() {
		return accountId;
	}

	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}

	public Integer getAccountType() {
		return accountType;
	}

	public void setAccountType(Integer accountType) {
		this.accountType = accountType;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}
}
