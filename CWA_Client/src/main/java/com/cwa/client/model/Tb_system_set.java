package com.cwa.client.model;

public class Tb_system_set {

	private Integer id;
	private String system_key;// 键
	private String system_value;// 值
	private String comment;// 字段说明

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getSystem_key() {
		return system_key;
	}

	public void setSystem_key(String system_key) {
		this.system_key = system_key;
	}

	public String getSystem_value() {
		return system_value;
	}

	public void setSystem_value(String system_value) {
		this.system_value = system_value;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

}
